@section('parrafo2')
Si bien los Sistemas de Información pueden diferir en la forma en que se usan dentro de una organización, todos tienen los siguientes componentes:<br>
•	Hardware: los sistemas utilizan hardware local como una computadora o servicios de Nube para su ejecución.<br>
•	Software: estos son los programas utilizados para administración, procesamiento y análisis.<br>
•	Bases de datos: los sistemas trabajan con recursos organizados en tablas y archivos.<br>
•	Red: se deben conectar diferentes recursos entre sí, especialmente si muchas personas diferentes en una organización usan el mismo sistema.<br>
•	Procedimientos: describen cómo se procesan y analizan los datos y recursos específicos para obtener las respuestas para las que está diseñado el sistema (“lógica del negocio”).

